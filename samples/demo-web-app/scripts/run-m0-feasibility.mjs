import { spawnSync } from "node:child_process";
import { mkdirSync, rmSync, writeFileSync } from "node:fs";
import { dirname, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const repoRoot = resolve(root, "..", "..");
const artifactDir = resolve(repoRoot, "artifacts", "m0-feasibility");

function commandSpec(command, args) {
  if (process.platform === "win32" && command.endsWith(".cmd")) {
    return { command: process.env.ComSpec || "cmd.exe", args: ["/d", "/s", "/c", command, ...args] };
  }
  return { command, args };
}

function run(command, args, options = {}) {
  const spec = commandSpec(command, args);
  const startedAt = new Date();
  const result = spawnSync(spec.command, spec.args, {
    cwd: root,
    encoding: "utf8",
    stdio: options.capture ? "pipe" : "inherit",
    env: options.env ? { ...process.env, ...options.env } : process.env,
    shell: false
  });
  const endedAt = new Date();
  const record = {
    command: [command, ...args].join(" "),
    exitCode: result.status,
    startedAt: startedAt.toISOString(),
    endedAt: endedAt.toISOString(),
    durationMs: endedAt.getTime() - startedAt.getTime(),
    stdout: result.stdout ?? "",
    stderr: result.stderr ?? ""
  };
  if (result.error) {
    throw result.error;
  }
  if (result.status !== 0) {
    if (options.capture) {
      process.stdout.write(result.stdout ?? "");
      process.stderr.write(result.stderr ?? "");
    }
    throw new Error(`${record.command} failed with exit code ${result.status ?? "unknown"}`);
  }
  return record;
}

function cleanupDatabase(name) {
  for (const suffix of ["", "-journal", "-wal", "-shm"]) {
    rmSync(resolve(root, "prisma", `${name}.db${suffix}`), { force: true });
  }
}

function parseDefects(stdout) {
  const marker = '{\n  "scenarios"';
  const index = stdout.lastIndexOf(marker);
  if (index < 0) {
    throw new Error("Could not find defect scenario JSON in verifier output.");
  }
  return JSON.parse(stdout.slice(index));
}

function parseLastLineJson(stdout) {
  const line = stdout.trim().split(/\r?\n/).at(-1);
  return JSON.parse(line);
}

function markdownReport(result) {
  const rows = result.defects.scenarios
    .map((scenario) => {
      const observed =
        typeof scenario.observed === "string"
          ? scenario.observed
          : Object.entries(scenario.observed)
              .map(([key, value]) => `${key}=${value}`)
              .join(", ");
      return `| \`${scenario.scenario}\` | ${scenario.status} | ${observed} |`;
    })
    .join("\n");

  return `# M0 feasibility experiment report

- Generated on: ${result.generatedOn}
- Repo commit: ${result.commit}
- Baseline status: ${result.baseline.status}
- Overall conclusion: ${result.conclusion.status}

## Baseline evidence

\`\`\`json
${JSON.stringify(result.baseline.observed, null, 2)}
\`\`\`

## Defect evidence

| Scenario | Status | Observed evidence |
| --- | --- | --- |
${rows}

## Commands

${result.commands.map((command) => `- \`${command.command}\` → exit ${command.exitCode}, ${command.durationMs} ms`).join("\n")}

## Conclusion

${result.conclusion.summary}

## Limitations

${result.limitations.map((item) => `- ${item}`).join("\n")}
`;
}

mkdirSync(artifactDir, { recursive: true });
cleanupDatabase("m0-baseline");

const commands = [];
const baselineEnv = { DATABASE_URL: "file:./prisma/m0-baseline.db" };
commands.push(run("node", ["scripts/init-db.mjs", "--reset"], { env: baselineEnv, capture: true }));
const baselineProbe = run("pnpm.cmd", ["exec", "tsx", "scripts/probe-m0-feasibility.mjs"], {
  env: baselineEnv,
  capture: true
});
commands.push(baselineProbe);
const baseline = parseLastLineJson(baselineProbe.stdout);

const defectRun = run("node", ["scripts/verify-defect-scenarios.mjs"], { capture: true });
commands.push(defectRun);
const defects = parseDefects(defectRun.stdout);

const commit = run("git", ["rev-parse", "HEAD"], { capture: true }).stdout.trim();
const result = {
  generatedOn: new Date().toISOString(),
  commit,
  baseline,
  defects,
  commands: commands.map(({ command, exitCode, startedAt, endedAt, durationMs }) => ({
    command,
    exitCode,
    startedAt,
    endedAt,
    durationMs
  })),
  conclusion: {
    status: baseline.status === "passed" && defects.scenarios.every((scenario) => scenario.status === "reproduced") ? "passed" : "failed",
    summary:
      "The correct baseline passes the minimum API/database/security/test-integrity checks, and each official defect scenario is distinguishable by at least one planned MVP evidence capability."
  },
  limitations: [
    "This is a local M0 feasibility experiment, not the M1 Docker Runner.",
    "Evidence is generated by local scripts, not yet by a trusted Manifest control plane.",
    "Browser evidence is deferred to M3; this experiment uses API, database, test diff, random input and repeated patch replay evidence.",
    "The public failure-case candidates remain draft/source_pending/verified=false and are not marked reproduced by this experiment."
  ]
};

writeFileSync(resolve(artifactDir, "summary.json"), `${JSON.stringify(result, null, 2)}\n`, "utf8");
writeFileSync(resolve(artifactDir, "report.md"), markdownReport(result), "utf8");
cleanupDatabase("m0-baseline");

console.log(`Wrote ${relative(repoRoot, resolve(artifactDir, "summary.json"))}`);
console.log(`Wrote ${relative(repoRoot, resolve(artifactDir, "report.md"))}`);
console.log(JSON.stringify({ status: result.conclusion.status, scenarios: defects.scenarios.length }));
